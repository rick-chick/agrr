import { readdir, readFile } from 'node:fs/promises';
import { join, basename, dirname, resolve } from 'node:path';

import { CAPTURE_LOCALES, agentPngFilename } from '../e2e/capture-locales.mjs';

/** SPA 内リダイレクトのみ。ログイン UI は `/login` で E2E する */
export const E2E_EXCLUDE_MANIFEST_PATTERNS = new Set(['auth/login']);

/**
 * コンポーネントを持たないリダイレクト専用 pattern のホストセレクタ上書き。
 * 新規ルート追加時は通常ここへの追記は不要（loadComponent / component から自動導出）。
 */
export const HOST_SELECTOR_OVERRIDES = {
  'public-plans/select-farm-size': 'app-public-plan-create',
};

const HOST_SELECTOR_OUT = 'e2e/host-selector-by-pattern.generated.ts';

export function patternToUrl(pattern) {
  if (pattern === '') return '/';
  if (pattern === '**') return '/__e2e-route-manifest-not-found__';
  if (pattern === 'public-plans/optimizing') return '/public-plans/optimizing?planId=1';
  if (pattern === 'public-plans/results') return '/public-plans/results?planId=1';
  if (pattern === 'entry-schedule/crop/:cropId') return '/entry-schedule/crop/1?farmId=1';
  let p = pattern.replace(/:[a-zA-Z_][a-zA-Z0-9_]*/g, '1');
  if (!p.startsWith('/')) p = `/${p}`;
  return p;
}

export async function parseRoutesFile(filePath) {
  const text = await readFile(filePath, 'utf8');
  const clean = text
    .replace(/\/\*[\s\S]*?\*\//g, '')
    .replace(/\/\/[^\n]*/g, '');
  const pathRegex = /\bpath:\s*['"]([^'"]*)['"]/g;
  const positions = [];
  let m;
  while ((m = pathRegex.exec(clean)) !== null) {
    positions.push({ path: m[1], index: m.index });
  }
  const routes = [];
  for (let i = 0; i < positions.length; i++) {
    const start = positions[i].index;
    const end = i + 1 < positions.length ? positions[i + 1].index : clean.length;
    const segment = clean.slice(start, end);
    const requiresAuth = /\bauthGuard\b/.test(segment);
    const pattern = positions[i].path;
    const componentImportPath = resolveComponentImportPath(segment, text, filePath);
    routes.push({
      pattern,
      url: patternToUrl(pattern),
      requiresAuth,
      source: basename(filePath),
      componentImportPath,
    });
  }
  return routes;
}

export function parseSelectorFromComponentSource(source) {
  const match = source.match(/\bselector:\s*['"]([^'"]+)['"]/);
  return match ? match[1] : null;
}

function resolveRelativeImport(importPath, fromFile) {
  const absolute = resolve(dirname(fromFile), importPath);
  return absolute.endsWith('.ts') ? absolute : `${absolute}.ts`;
}

function resolveComponentImportPath(segment, routesFileText, routesFilePath) {
  if (/\bredirectTo\b/.test(segment)) {
    return null;
  }

  const loadImport = segment.match(/import\s*\(\s*['"]([^'"]+)['"]\s*\)/);
  if (loadImport) {
    return resolveRelativeImport(loadImport[1], routesFilePath);
  }

  const componentRef = segment.match(/\bcomponent:\s*(\w+)/);
  if (componentRef) {
    const name = componentRef[1];
    const importRegex = new RegExp(
      `import\\s*\\{[^}]*\\b${name}\\b[^}]*\\}\\s*from\\s*['"]([^'"]+)['"]`,
    );
    const importMatch = routesFileText.match(importRegex);
    if (importMatch) {
      return resolveRelativeImport(importMatch[1], routesFilePath);
    }
  }

  return null;
}

async function selectorForComponentImportPath(componentImportPath) {
  const source = await readFile(componentImportPath, 'utf8');
  const selector = parseSelectorFromComponentSource(source);
  if (!selector) {
    throw new Error(`selector not found in ${componentImportPath}`);
  }
  return selector;
}

export async function buildHostSelectorData(frontendRoot) {
  const routesDir = join(frontendRoot, 'src/app/routes');
  const files = (await readdir(routesDir)).filter((f) => f.endsWith('.routes.ts')).sort();
  const all = [];
  for (const f of files) {
    all.push(...(await parseRoutesFile(join(routesDir, f))));
  }

  const map = { ...HOST_SELECTOR_OVERRIDES };
  for (const route of all) {
    if (E2E_EXCLUDE_MANIFEST_PATTERNS.has(route.pattern)) continue;
    if (!route.componentImportPath) continue;
    map[route.pattern] = await selectorForComponentImportPath(route.componentImportPath);
  }

  return { map, content: formatHostSelectorTs(map) };
}

export function formatHostSelectorTs(map) {
  const entries = Object.entries(map).sort(([a], [b]) => a.localeCompare(b));
  const lines = entries.map(([pattern, selector]) => {
    const key = /^[A-Za-z_][\w]*$/.test(pattern) ? pattern : `'${pattern}'`;
    return `  ${key}: '${selector}',`;
  });
  return [
    '/** AUTO-GENERATED by `npm run e2e:manifest` — do not edit. */',
    'export const HOST_SELECTOR_BY_PATTERN_GENERATED: Record<string, string> = {',
    ...lines,
    '};',
    '',
  ].join('\n');
}

export function buildRouteToPngMarkdown(deduped) {
  const lines = [
    '# route-manifest → agent-review/out PNG ファイル名',
    '',
    '**この表は `npm run e2e:manifest` で自動生成する。** Agent はユーザーに URL やページ指定を求めず、この表と `e2e/route-manifest.json` を正とする。',
    '',
    '## ファイル名規則（`e2e/visual/route-manifest-visual.spec.ts` と同一）',
    '',
    '- ベース名: `pattern` が空文字 → `home` / `**` → `not-found` / それ以外は `[a-zA-Z0-9_.-]` 以外を `_` に置換',
    `- 出力: \`{ベース名}.{locale}.png\`（locale: ${CAPTURE_LOCALES.join(', ')}。in はヒンディー語・インド向け）`,
    '',
    '## 全ルート一覧',
    '',
    '| # | pattern | url (E2E goto) | requiresAuth | ja | en | in |',
    '|---|---------|----------------|--------------|----|----|-----|',
  ];
  deduped.forEach((r, i) => {
    const esc = (s) => String(s).replace(/\|/g, '\\|');
    const patDisp = r.pattern === '' ? '(home)' : r.pattern;
    const pngCols = CAPTURE_LOCALES.map(
      (locale) => `\`${esc(agentPngFilename(r.pattern, locale))}\``,
    ).join(' | ');
    lines.push(
      `| ${i + 1} | \`${esc(patDisp)}\` | \`${esc(r.url)}\` | ${r.requiresAuth ? 'yes' : 'no'} | ${pngCols} |`,
    );
  });
  lines.push('');
  lines.push('## キャプチャ前提');
  lines.push('');
  lines.push(
    '- `e2e:capture-for-agent` は **`E2E_CAPTURE_DEV_SESSION=1`** で Rails（127.0.0.1:3000）と ng を起動し、globalSetup が **`e2e/.auth/dev-session.json`** を書き出したうえで各 `url` へ **ja / en / in** の順で遷移し `out/{ベース}.{locale}.png` を書き出す（`/api/v1/auth/me` はモックしない）。',
  );
  lines.push(
    '- `e2e/resolve-capture-urls.ts` が一覧 API から実在 id を取りマニフェストの placeholder を差し替える。DB が空や API 不全のときは画面が薄い・エラーになり得る。',
  );
  lines.push('');
  return lines.join('\n');
}

export async function buildManifestData(frontendRoot) {
  const routesDir = join(frontendRoot, 'src/app/routes');
  const files = (await readdir(routesDir)).filter((f) => f.endsWith('.routes.ts')).sort();
  const all = [];
  for (const f of files) {
    all.push(...(await parseRoutesFile(join(routesDir, f))));
  }

  const seen = new Set();
  const deduped = [];
  for (const r of all) {
    if (E2E_EXCLUDE_MANIFEST_PATTERNS.has(r.pattern)) continue;
    const k = `${r.pattern}\0${r.requiresAuth}`;
    if (seen.has(k)) continue;
    seen.add(k);
    deduped.push(r);
  }

  deduped.sort((a, b) => {
    if (a.requiresAuth !== b.requiresAuth) return a.requiresAuth ? 1 : -1;
    return a.url.localeCompare(b.url);
  });

  const payload = {
    generatedAt: new Date().toISOString(),
    note:
      'path の :param は URL では 1 に置換。`**` は意図的な 404 用パス。Agent 用 PNG は `npm run e2e:capture-for-agent`（Rails development + AuthTest モックログインで付与したセッション、`e2e/.auth/dev-session.json`）で全ルートを ja/en/in の3言語キャプチャする。requiresAuth はルート定義上のフラグ。',
    routes: deduped,
  };

  return {
    payload,
    routeToPngContent: buildRouteToPngMarkdown(deduped),
  };
}

/** generatedAt を除き commit 済み manifest と比較する。 */
export function normalizeManifestJson(obj) {
  return JSON.stringify({ note: obj.note, routes: obj.routes }, null, 2);
}

export async function checkManifestFreshness(frontendRoot) {
  const manifestPath = join(frontendRoot, 'e2e/route-manifest.json');
  const routeToPngPath = join(frontendRoot, 'e2e/agent-review/route-to-png.md');
  const hostSelectorPath = join(frontendRoot, HOST_SELECTOR_OUT);
  const { payload, routeToPngContent } = await buildManifestData(frontendRoot);
  const { content: hostSelectorContent } = await buildHostSelectorData(frontendRoot);
  const existingManifest = JSON.parse(await readFile(manifestPath, 'utf8'));
  const existingRouteToPng = await readFile(routeToPngPath, 'utf8');
  const existingHostSelector = await readFile(hostSelectorPath, 'utf8');

  const errors = [];
  if (normalizeManifestJson(payload) !== normalizeManifestJson(existingManifest)) {
    errors.push('e2e/route-manifest.json is out of date. Run: npm run e2e:manifest');
  }
  if (routeToPngContent !== existingRouteToPng) {
    errors.push('e2e/agent-review/route-to-png.md is out of date. Run: npm run e2e:manifest');
  }
  if (hostSelectorContent !== existingHostSelector) {
    errors.push(
      'e2e/host-selector-by-pattern.generated.ts is out of date. Run: npm run e2e:manifest',
    );
  }

  return { ok: errors.length === 0, errors };
}
